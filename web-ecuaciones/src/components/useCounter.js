import {useState} from 'react';

export const useCounter = (initState = 0) => {
    const [state, setState] = useState(initState);

    const increment = (factor = 1) => {
        setState(state + factor)
    }

    const reset = () => {
        setState(initState)
    }

    const decrement = (factor = 1) => {
        setState(state - factor)
    }

    return {
        state,
        increment,
        decrement,
        reset
    };
}

